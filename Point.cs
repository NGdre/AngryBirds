
class Point
{
    public double X;
    public double Y;
    public double Vx;
    public double Vy;
    public Point(double x, double y)
    {
        this.X = x;
        this.Y = y;
    }
    public Point(double x, double y, double Vx, double Vy)
    {
        this.X = x;
        this.Y = y;
        this.Vx = Vx;
        this.Vy = Vy;
    }
}

